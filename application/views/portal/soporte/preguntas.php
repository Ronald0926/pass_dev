                        <h5 class="pregunta">11. ¿Cómo realizar solicitud de tarjetas por proceso masivo?</h5>
                    </a>
                    <ul id="11Link" class="collapse">
                        <p>
                            Desde la opción solicitud masiva, debe descargar el archivo en Excel, validar la matriz de código y las instrucciones allí plasmadas.
                            Debe diligenciar los campos sin carácteres especiales o signos de puntuación; (en caso de que la casilla no requiera información, debe colocar un guión -)
                            Los datos que pide la plantilla son:</p>
                        <ul>
                            <li>Producto: (lo debe seleccionar de la lista desplegable y no pueden ser diferentes a los productos que se registraron en la cotización previa)
                            </li>
                            <li>Tipo de documento del tarjetahabiente (Deebe tener en cuenta la matriz de códigos plasmada en la hoja 3 de la plantilla denominada <span style="font-weight: bold">Códigos</span>)
                            </li>
                            <li>Documento del tarjetahabiente (los números los debe digitar sin estar separados por puntos)
                            </li>
                            <li>Nombres y apellidos (puede digitarse el primer nombre y el primer apellido únicamente o si se requiere el nombre completo; dependiendo el caso los demás campos van con guion –
                            </li>
                            <li>Correo electrónico del tarjeta habiente o de la empresa según corresponda
                            </li>
                            <li>Teléfono de contacto
                            </li>
                            <li>Identificador (este campo aplica únicamente para tarjetas Business cuyo realce sale a nombre de la empresa y es ella quien define como nombrarlas)
                            </li>
                            <li>Valor abono dependiendo de si este proceso lo realiza de inmediato o de manera posterior
                            </li>
                            <li>Fecha de dispersión corresponde a la fecha en que desean que sean abonadas las tarjetas 
                            </li>
                            <li>Tipo de documento del custodio </li>
                            <li>Documento del custodio (debe tener en cuenta qué custodio le asignó al tarjetahabiente)
                            </li>
                        </ul>
                        <p>
                            Debe tener en cuenta las instrucciones plasmadas en la hoja 2 de la plantilla denominada  <span style="font-weight: bold">instrucciones.</span>

                            Luego de diligenciar la plantilla, debe guardar y cargar el archivo. El sistema NO notifica que la carga fue exitosa; únicamente se debe seleccionar la opción enviar y luego finalizar.
                        </p>
